/* ── i18n helpers ─────────────────────────────────────────────────────── */
function getLang() { return localStorage.getItem('sg_lang') || 'en' }
function setLang(l) { localStorage.setItem('sg_lang', l); location.reload() }
function t(key) {
  const lang = getLang()
  return (TRANSLATIONS[lang] && TRANSLATIONS[lang][key]) || TRANSLATIONS['en'][key] || key
}

/* ── Apply lang attribute + load Indic font on every page load ─────────── */
;(function () {
  const lang = getLang()

  // 1. Set html[lang] for screen readers, spell-check, and OS font hinting
  document.documentElement.lang = lang

  // 2. Dynamically inject the correct Noto Sans subset for Indic scripts
  const INDIC_FONTS = {
    ta: 'Noto+Sans+Tamil:wght@400;500;600;700',
    te: 'Noto+Sans+Telugu:wght@400;500;600;700',
    kn: 'Noto+Sans+Kannada:wght@400;500;600;700',
    ml: 'Noto+Sans+Malayalam:wght@400;500;600;700',
    hi: 'Noto+Sans+Devanagari:wght@400;500;600;700'
  }
  if (INDIC_FONTS[lang]) {
    const link = document.createElement('link')
    link.rel = 'stylesheet'
    link.href = 'https://fonts.googleapis.com/css2?family=' + INDIC_FONTS[lang] + '&display=swap'
    document.head.appendChild(link)

    // Prepend Noto font so it takes priority over Plus Jakarta Sans for Indic glyphs
    const NOTO_FAMILY = {
      ta: "'Noto Sans Tamil'",
      te: "'Noto Sans Telugu'",
      kn: "'Noto Sans Kannada'",
      ml: "'Noto Sans Malayalam'",
      hi: "'Noto Sans Devanagari'"
    }
    document.documentElement.style.setProperty(
      '--font',
      NOTO_FAMILY[lang] + ",'Plus Jakarta Sans',system-ui,sans-serif"
    )
  }
})();
